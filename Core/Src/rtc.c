// 设置多少秒后唤醒
void RTC_SetAlarm_Relative(uint32_t seconds) {
    
    RTC_AlarmTypeDef sAlarm = {0};
    RTC_TimeTypeDef sTime = {0};
    RTC_DateTypeDef sDate = {0}; // F103获取时间必须同时调用GetDate，否则寄存器不锁定

    // 1. 获取当前时间
    HAL_RTC_GetTime(&hrtc, &sTime, RTC_FORMAT_BIN);
    HAL_RTC_GetDate(&hrtc, &sDate, RTC_FORMAT_BIN);

    // 2. 计算闹钟时间点
    // 注意：这里需要处理秒、分、时的进位
    uint32_t total_seconds = sTime.Hours * 3600 + sTime.Minutes * 60 + sTime.Seconds;
    total_seconds += seconds;

    sAlarm.AlarmTime.Hours   = (total_seconds / 3600) % 24;
    sAlarm.AlarmTime.Minutes = (total_seconds % 3600) / 60;
    sAlarm.AlarmTime.Seconds = total_seconds % 60;

    // 3. 设置闹钟
    if (HAL_RTC_SetAlarm_IT(&hrtc, &sAlarm, RTC_FORMAT_BIN) != HAL_OK) {
        Error_Handler();
    }
}

